	public function switchLang($lang,Request $request){
        $lanid        =    DB::table("languages")->where('lang_code', '=', $lang)->first()->id;
        session()->put('sel_lang',$lanid);
        Session::put('admin_applocale', $lang);
        if(Auth::guard('admin')->user()){
            $admin_modules	=	$this->buildTree();
            Session()->put('acls',$admin_modules);
        }
        Session::put('applocale', $lang);
        App::setLocale($lang);
        $language = Language::where('lang_code', $lang)->first();
        if ($language) {
            $laniduser = $language->id;
        }else{
            $laniduser = 1;
        }
        Session::put('currentLanguageId', $laniduser);

		if(Auth::check()) {
			$user  = Auth::user();
			$user->language  =  $laniduser;
			$user->save();
		}
        return Redirect::back();
    }
